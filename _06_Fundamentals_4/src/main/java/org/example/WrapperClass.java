package org.example;

public class WrapperClass {

    // Classes representation of primitive data types are called as Wrapper Classes.
    // java.lang

    public static void main(String[] args) {


        int num1 = 2; // primitive data type
        Integer num2 = null; // Class representation of int

        float val = 12.2f;
        Float val2 = 13.12f;

        // byte, short, int , long
        // Byte, Short, Integer, Long

        // char, boolean
//        Character, Boolean




        System.out.println(num1); // 2
        System.out.println(num2.MIN_VALUE); // 5


        Byte num = 0;
        System.out.println(num.MAX_VALUE);




    }

}
